package main

import (
	"fmt"
	"log"
	"time"
)

// AddOfferFollowUp creates a new follow-up reminder for an offer
func (a *App) AddOfferFollowUp(offerID, followUpDate, notes string) (*OfferFollowUp, error) {
	if err := a.requirePermission("offers:create"); err != nil {
		return nil, err
	}

	if a.db == nil {
		return nil, fmt.Errorf("database not initialized")
	}

	// Validate offer exists
	var offer Offer
	if err := a.db.First(&offer, "id = ?", offerID).Error; err != nil {
		return nil, fmt.Errorf("offer not found: %w", err)
	}

	// Parse follow-up date
	parsedDate, err := time.Parse("2006-01-02", followUpDate)
	if err != nil {
		return nil, fmt.Errorf("invalid date format (expected YYYY-MM-DD): %w", err)
	}

	// Create follow-up record (ID auto-generated by Base.BeforeCreate hook)
	followUp := &OfferFollowUp{
		OfferID:      offerID,
		FollowUpDate: parsedDate,
		Notes:        notes,
		Status:       "pending",
	}

	if err := a.db.Create(followUp).Error; err != nil {
		log.Printf("Failed to create offer follow-up: %v", err)
		return nil, fmt.Errorf("failed to create follow-up: %w", err)
	}

	log.Printf("Created follow-up for offer %s on %s", offerID, followUpDate)
	return followUp, nil
}

// GetOfferFollowUps returns all follow-ups for a specific offer
func (a *App) GetOfferFollowUps(offerID string) ([]OfferFollowUp, error) {
	if err := a.requirePermission("offers:view"); err != nil {
		return nil, err
	}

	if a.db == nil {
		return nil, fmt.Errorf("database not initialized")
	}

	var followUps []OfferFollowUp
	if err := a.db.Where("offer_id = ?", offerID).
		Order("follow_up_date ASC").
		Find(&followUps).Error; err != nil {
		log.Printf("Failed to get follow-ups for offer %s: %v", offerID, err)
		return nil, fmt.Errorf("failed to get follow-ups: %w", err)
	}

	return followUps, nil
}

