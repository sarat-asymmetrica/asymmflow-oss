// GetOverdueOfferFollowUps returns pending offer follow-ups that are past their due date
func (a *App) GetOverdueOfferFollowUps() ([]OfferFollowUp, error) {
	if err := a.requirePermission("offers:view"); err != nil {
		return nil, err
	}

	if a.db == nil {
		return nil, fmt.Errorf("database not initialized")
	}

	now := time.Now()
	var followUps []OfferFollowUp
	if err := a.db.Where("status = ? AND follow_up_date < ?", "pending", now).
		Order("follow_up_date ASC").
		Find(&followUps).Error; err != nil {
		log.Printf("Failed to get overdue follow-ups: %v", err)
		return nil, fmt.Errorf("failed to get overdue follow-ups: %w", err)
	}

	return followUps, nil
}

// CompleteOfferFollowUp marks a follow-up as completed
func (a *App) CompleteOfferFollowUp(followUpID string) error {
	if err := a.requirePermission("offers:create"); err != nil {
		return err
	}

	if a.db == nil {
		return fmt.Errorf("database not initialized")
	}

	var followUp OfferFollowUp
	if err := a.db.First(&followUp, "id = ?", followUpID).Error; err != nil {
		return fmt.Errorf("follow-up not found: %w", err)
	}

	now := time.Now()
	followUp.Status = "completed"
	followUp.CompletedAt = &now
	// TODO: Set CompletedBy from current user context when auth is ready

	if err := a.db.Save(&followUp).Error; err != nil {
		log.Printf("Failed to complete follow-up %s: %v", followUpID, err)
		return fmt.Errorf("failed to complete follow-up: %w", err)
	}

	log.Printf("Completed follow-up %s", followUpID)
	return nil
}

