// GenerateDashboardReport generates a PDF report of dashboard statistics
func (a *App) GenerateDashboardReport() (string, error) {
	// I-26: exported/bound report generator — gate read access. The internal
	// email surfaces (SendReportByEmail/CreateReportDraft) already gate with
	// reports:export, which is granted only via the admin wildcard that also
	// grants reports:view, so this adds no double-gate regression.
	if err := a.requirePermission("reports:view"); err != nil {
		return "", err
	}
	gen := NewReportGenerator()
	gen.letterheadPath = a.letterheadImagePath()

	if err := gen.loadFonts(); err != nil {
		return "", fmt.Errorf("failed to load fonts: %v", err)
	}

	gen.pdf.AddPage()

	// Header (with letterhead background)
	gen.drawHeader("Dashboard Report", "Payment Prediction Analytics Overview")

	// Get statistics
	stats, err := a.GetStatistics()
	if err != nil {
		return "", fmt.Errorf("failed to get statistics: %v", err)
	}

	dashStats, err := a.GetDashboardStats()
	if err != nil {
		return "", fmt.Errorf("failed to get dashboard stats: %v", err)
	}

	// Key Metrics Section (start below letterhead if present)
	startY := 90.0
	if gen.letterheadPath != "" {
		startY = 175.0
	}
	currentY := gen.drawSectionTitle("Key Metrics", startY)

	gen.drawKeyValuePair("Total Predictions", fmt.Sprintf("%d", stats.TotalPredictions), 40, currentY)
	currentY += 20

	gen.drawKeyValuePair("Average Confidence", fmt.Sprintf("%.1f%%", stats.AvgConfidence*100), 40, currentY)
	currentY += 20

	gen.drawKeyValuePair("Average Payment Days", fmt.Sprintf("%.0f days", stats.AvgPredictedDays), 40, currentY)
	currentY += 20

	gen.drawKeyValuePair("Win Rate", fmt.Sprintf("%.1f%%", dashStats.WinRate), 40, currentY)
	currentY += 30

	// Grade Distribution Section
	currentY = gen.drawSectionTitle("Grade Distribution", currentY)

	for grade, count := range stats.GradeDistribution {
		percentage := float64(count) / float64(stats.TotalPredictions) * 100
		gen.drawKeyValuePair(
			fmt.Sprintf("Grade %s", grade),
			fmt.Sprintf("%d (%.1f%%)", count, percentage),
			40,
			currentY,
		)
		currentY += 18
	}

	currentY += 20

	// Recent Activity Section
	currentY = gen.drawSectionTitle("Recent Activity", currentY)
	currentY += 10

	records, err := a.GetHistory(10)
	if err != nil {
		return "", fmt.Errorf("failed to get history: %v", err)
	}
	if len(records) > 0 {
		headers := []string{"Date", "Customer", "Grade", "Days", "Confidence"}
		colWidths := []float64{80, 200, 60, 60, 80}

		rows := [][]string{}
		for _, record := range records {
			rows = append(rows, []string{
				record.CreatedAt.Format("2006-01-02"),
				record.CustomerName,
				record.Grade,
				fmt.Sprintf("%d", record.PredictedDays),
				fmt.Sprintf("%.1f%%", record.Confidence*100),
			})
		}

		currentY = gen.drawTable(headers, rows, currentY, colWidths)
	}

	// Footer
	gen.drawFooter(1)

	// Save to user's Documents folder
	documentsPath, err := os.UserHomeDir()
	if err != nil {
		documentsPath = "."
	} else {
		documentsPath = filepath.Join(documentsPath, "Documents")
	}

	timestamp := time.Now().Format("20060102_150405")
	filename := fmt.Sprintf("PH_Holdings_Dashboard_%s.pdf", timestamp)
	outputPath := filepath.Join(documentsPath, filename)

	if err := gen.pdf.WritePdf(outputPath); err != nil {
		return "", fmt.Errorf("failed to write PDF: %v", err)
	}

	return outputPath, nil
}

