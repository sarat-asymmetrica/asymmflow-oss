// calculateAverageGrade calculates the average grade from records
func calculateAverageGrade(records []PredictionRecord) string {
	if len(records) == 0 {
		return "N/A"
	}

	gradeMap := map[string]int{"A": 4, "B": 3, "C": 2, "D": 1}
	reverseMap := map[int]string{4: "A", 3: "B", 2: "C", 1: "D"}

	total := 0
	for _, record := range records {
		total += gradeMap[record.Grade]
	}

	avg := total / len(records)
	return reverseMap[avg]
}

// calculateAverageDays calculates the average predicted days from records
func calculateAverageDays(records []PredictionRecord) float64 {
	if len(records) == 0 {
		return 0
	}

	total := 0
	for _, record := range records {
		total += record.PredictedDays
	}

	return float64(total) / float64(len(records))
}

// Note: truncateText and fileExists are defined in pdf_generator.go

// sanitizeFilename removes invalid characters from filenames
func sanitizeFilename(name string) string {
	// Replace spaces and special chars with underscores
	result := ""
	for _, ch := range name {
		if (ch >= 'a' && ch <= 'z') || (ch >= 'A' && ch <= 'Z') || (ch >= '0' && ch <= '9') {
			result += string(ch)
		} else {
			result += "_"
		}
	}

	// Truncate to reasonable length
	if len(result) > 30 {
		result = result[:30]
	}

	return result
}

