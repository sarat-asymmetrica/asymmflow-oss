// GetReportData retrieves report data based on type and date range
func (a *App) GetReportData(reportType string, dateRange string) (ReportData, error) {
	// I-26: this exported/bound getter returns the same underlying finance,
	// sales, customer and operations data the exporters gate — so it must gate
	// too. Map each report type to the permission that guards its data
	// (requireReportAccess), matching ExportReport and deployed PH.
	if err := a.requireReportAccess(reportType); err != nil {
		return ReportData{}, err
	}
	if a.db == nil {
		return ReportData{}, newError("DB_NOT_INITIALIZED", "Database not available", "")
	}

	var startDate time.Time
	now := time.Now()

	// Parse date range
	switch dateRange {
	case "week":
		startDate = now.AddDate(0, 0, -7)
	case "month":
		startDate = now.AddDate(0, -1, 0)
	case "quarter":
		startDate = now.AddDate(0, -3, 0)
	case "year":
		startDate = now.AddDate(-1, 0, 0)
	default:
		startDate = now.AddDate(0, -1, 0) // Default to month
	}

	switch reportType {
	case "sales":
		return a.getSalesReport(startDate)
	case "customers":
		return a.getCustomersReport(startDate)
	case "operations":
		return a.getOperationsReport(startDate)
	case "inventory":
		return a.getInventoryReport(startDate)
	case "financial":
		return a.getFinancialReport(startDate)
	default:
		return ReportData{}, newError("INVALID_REPORT_TYPE", "Unknown report type", reportType)
	}
}

// getSalesReport generates sales analytics
func (a *App) getSalesReport(startDate time.Time) (ReportData, error) {
	var data ReportData

	// Pipeline by Stage (from Offers table)
	type StageCount struct {
		Stage string
		Count int64
		Total float64
	}
	var stages []StageCount
	err := a.db.Model(&Offer{}).
		Select("stage, COUNT(*) as count, COALESCE(SUM(net_value_bhd), 0) as total").
		Where("created_at >= ?", startDate).
		Group("stage").
		Order("CASE stage WHEN 'RFQ' THEN 1 WHEN 'Quoted' THEN 2 WHEN 'Follow-up' THEN 3 WHEN 'Won' THEN 4 ELSE 5 END").
		Scan(&stages).Error

	if err == nil {
		data.Pipeline = make([]PipelineStage, len(stages))
		for i, s := range stages {
			data.Pipeline[i] = PipelineStage{
				Stage: s.Stage,
				Count: int(s.Count),
				Value: s.Total,
			}
		}
	}

	// Win rate calculation
	var totalOffers, wonOffers int64
	a.db.Model(&Offer{}).Where("created_at >= ?", startDate).Count(&totalOffers)
	a.db.Model(&Offer{}).Where("created_at >= ? AND stage = ?", startDate, "Won").Count(&wonOffers)
	if totalOffers > 0 {
		data.WinRate = float64(wonOffers) / float64(totalOffers)
	}

	// Average deal size
	var avgDeal float64
	a.db.Model(&Offer{}).
		Where("created_at >= ? AND stage = ?", startDate, "Won").
		Select("AVG(net_value_bhd)").
		Scan(&avgDeal)
	data.AvgDealSize = avgDeal

	// Conversion rate (RFQ → Won)
	var rfqCount int64
	a.db.Model(&Offer{}).Where("created_at >= ? AND stage = ?", startDate, "RFQ").Count(&rfqCount)
	if rfqCount > 0 {
		data.ConversionRate = float64(wonOffers) / float64(rfqCount)
	}

	// Top products (from offer items)
	type ProductSummary struct {
		ProductCode string
		Revenue     float64
		Quantity    float64
	}
	var products []ProductSummary
	err = a.db.Table("offer_items oi").
		Select("oi.product_code, SUM(oi.line_total_bhd) as revenue, SUM(oi.quantity) as quantity").
		Joins("JOIN offers o ON o.id = oi.offer_id").
		Where("o.created_at >= ? AND o.stage = ?", startDate, "Won").
		Group("oi.product_code").
		Order("revenue DESC").
		Limit(5).
		Scan(&products).Error

	if err == nil {
		data.TopProducts = make([]TopProduct, len(products))
		for i, p := range products {
			data.TopProducts[i] = TopProduct{
				Name:    p.ProductCode,
				Revenue: p.Revenue,
				Count:   int(p.Quantity),
			}
		}
	}

	return data, nil
}

// getCustomersReport generates customer analytics
func (a *App) getCustomersReport(startDate time.Time) (ReportData, error) {
	var data ReportData

	// Grade distribution
	type GradeCount struct {
		Grade string
		Count int64
	}
	var grades []GradeCount
	err := a.db.Model(&CustomerMaster{}).
		Select("payment_grade as grade, COUNT(*) as count").
		Where("payment_grade != ''").
		Group("payment_grade").
		Order("payment_grade").
		Scan(&grades).Error

	if err == nil {
		totalCustomers := int64(0)
		for _, g := range grades {
			totalCustomers += g.Count
		}

		data.GradeDistribution = make([]GradeDistItem, len(grades))
		for i, g := range grades {
			percentage := float64(0)
			if totalCustomers > 0 {
				percentage = (float64(g.Count) / float64(totalCustomers)) * 100
			}
			data.GradeDistribution[i] = GradeDistItem{
				Grade:      g.Grade,
				Count:      int(g.Count),
				Percentage: percentage,
			}
		}
	}

	// Customer type distribution
	type TypeCount struct {
		Type  string
		Count int64
	}
	var types []TypeCount
	err = a.db.Model(&CustomerMaster{}).
		Select("customer_type as type, COUNT(*) as count").
		Where("customer_type != ''").
		Group("customer_type").
		Scan(&types).Error

	if err == nil {
		typeLabels := map[string]string{
			"EC": "End Customer",
			"EP": "EPC/Licensor",
			"SI": "System Integrator",
			"NR": "National Reseller",
		}

		data.TypeDistribution = make([]CustomerTypeItem, len(types))
		for i, t := range types {
			label := typeLabels[t.Type]
			if label == "" {
				label = t.Type
			}
			data.TypeDistribution[i] = CustomerTypeItem{
				Type:  t.Type,
				Label: label,
				Count: int(t.Count),
			}
		}
	}

	// Average payment days
	var avgPayment float64
	a.db.Model(&CustomerMaster{}).
		Select("AVG(avg_payment_days)").
		Scan(&avgPayment)
	data.AvgPaymentDays = avgPayment

	// Collection efficiency (simplified - invoices paid within terms)
	var totalInvoices, paidOnTime int64
	a.db.Model(&Invoice{}).Where("created_at >= ?", startDate).Count(&totalInvoices)
	a.db.Model(&Invoice{}).
		Where("created_at >= ? AND paid_date IS NOT NULL", startDate).
		Count(&paidOnTime)

	if totalInvoices > 0 {
		data.CollectionEff = float64(paidOnTime) / float64(totalInvoices)
	}

	return data, nil
}

// getOperationsReport generates operations analytics
func (a *App) getOperationsReport(startDate time.Time) (ReportData, error) {
	var data ReportData

	// Orders by stage
	type StageCount struct {
		Status string
		Count  int64
	}
	var stages []StageCount
	err := a.db.Model(&Order{}).
		Select("status, COUNT(*) as count").
		Where("created_at >= ?", startDate).
		Group("status").
		Scan(&stages).Error

	if err == nil {
		data.OrdersByStage = make([]OrderStage, len(stages))
		for i, s := range stages {
			data.OrdersByStage[i] = OrderStage{
				Stage: s.Status,
				Count: int(s.Count),
			}
		}
	}

	// Average lead time (simplified - from order to delivery)
	var avgLead float64
	a.db.Table("orders o").
		Select("AVG(JULIANDAY(s.delivered_date) - JULIANDAY(o.order_date))").
		Joins("JOIN shipments s ON s.order_id = o.id").
		Where("o.created_at >= ? AND s.delivered_date IS NOT NULL", startDate).
		Scan(&avgLead)
	data.AvgLeadTime = int(avgLead)

	// On-time delivery rate
	var totalDelivered, onTime int64
	a.db.Table("orders o").
		Joins("JOIN shipments s ON s.order_id = o.id").
		Where("o.created_at >= ? AND s.delivered_date IS NOT NULL", startDate).
		Count(&totalDelivered)

	a.db.Table("orders o").
		Joins("JOIN shipments s ON s.order_id = o.id").
		Where("o.created_at >= ? AND s.delivered_date IS NOT NULL AND s.delivered_date <= o.promised_date", startDate).
		Count(&onTime)

	if totalDelivered > 0 {
		data.OnTimeDelivery = float64(onTime) / float64(totalDelivered)
	}

	// Pending shipments
	var pending int64
	a.db.Model(&Shipment{}).
		Where("status IN ?", []string{"Packed", "Shipped", "In Transit"}).
		Count(&pending)
	data.PendingShipments = int(pending)

	return data, nil
}

// getInventoryReport generates inventory analytics
func (a *App) getInventoryReport(startDate time.Time) (ReportData, error) {
	var data ReportData

	// Total items and value (from products table)
	var totalItems int64
	var totalValue float64
	a.db.Model(&ProductMaster{}).
		Where("is_stocked = ?", true).
		Count(&totalItems)

	a.db.Model(&ProductMaster{}).
		Where("is_stocked = ?", true).
		Select("SUM(stock_quantity * standard_cost_bhd)").
		Scan(&totalValue)

	data.TotalItems = int(totalItems)
	data.TotalValue = totalValue

	// Low stock alerts (products with stock below threshold)
	var lowStock int64
	a.db.Model(&ProductMaster{}).
		Where("is_stocked = ? AND stock_quantity < ?", true, 5).
		Count(&lowStock)
	data.LowStockAlerts = int(lowStock)

	// Stock movements within the report window, aggregated by direction from
	// the real stock_movements ledger (RecordStockMovement) — replaces the
	// previous hardcoded placeholder rows.
	type MovementAgg struct {
		Direction string
		Count     int64
		Value     float64
	}
	var movementAggs []MovementAgg
	err := a.db.Model(&StockMovement{}).
		Select("direction, COUNT(*) as count, COALESCE(SUM(total_value), 0) as value").
		Where("movement_date >= ?", startDate).
		Group("direction").
		Scan(&movementAggs).Error

	if err == nil {
		data.Movements = make([]StockMovementSummary, len(movementAggs))
		for i, m := range movementAggs {
			data.Movements[i] = StockMovementSummary{
				Type:  m.Direction,
				Count: int(m.Count),
				Value: m.Value,
			}
		}
	}

	return data, nil
}

// getFinancialReport generates financial analytics in trader vocabulary —
// receivables/payables/collection, not SaaS runway/burn/MRR framing. Every
// figure below is a direct query result; nothing is an assumed-margin guess
// dressed up as fact (the old Runway card was a hardcoded 12.5, and Cash
// Balance/Monthly Burn were revenue*0.3 estimates — all three are gone).
func (a *App) getFinancialReport(startDate time.Time) (ReportData, error) {
	var data ReportData

	// Avg Monthly Revenue — real average of paid invoice totals per calendar
	// month in the window (previously mislabeled "MRR" with a fictional
	// 8000 BHD target bolted on; the target is gone, the number is real).
	var avgMonthlyRevenue float64
	a.db.Model(&Invoice{}).
		Where("created_at >= ? AND status = ?", startDate, "Paid").
		Select("SUM(grand_total_bhd) / COUNT(DISTINCT strftime('%Y-%m', invoice_date))").
		Scan(&avgMonthlyRevenue)
	data.AvgMonthlyRevenue = avgMonthlyRevenue

	// Collection metrics
	var collectionTarget, collected float64
	a.db.Model(&Invoice{}).
		Where("created_at >= ?", startDate).
		Select("SUM(grand_total_bhd)").
		Scan(&collectionTarget)

	a.db.Model(&Invoice{}).
		Where("created_at >= ? AND status = ?", startDate, "Paid").
		Select("SUM(paid_amount_bhd)").
		Scan(&collected)

	data.CollectionTarget = collectionTarget
	data.Collected = collected

	// Receivables Outstanding — real sum of unpaid invoice balances. Replaces
	// the old hardcoded Runway card outright (no margin/assumption factor).
	var totalReceivables float64
	a.db.Model(&Invoice{}).
		Where("status != ?", "Paid").
		Select("SUM(outstanding_bhd)").
		Scan(&totalReceivables)
	data.ReceivablesOutstanding = totalReceivables

	// Payables Outstanding — real sum of unpaid supplier invoices, mirroring
	// GetAPAgingReport's own unpaid-invoice query. Replaces the old Cash
	// Balance/Monthly Burn cards, which were revenue*0.3 guesses.
	var totalPayables float64
	a.db.Model(&SupplierInvoice{}).
		Where("payment_status != ? AND deleted_at IS NULL", "Paid").
		Select("SUM(total_bhd)").
		Scan(&totalPayables)
	data.PayablesOutstanding = totalPayables

	// Overdue receivables by aging bucket
	type OverdueSum struct {
		Bucket string
		Amount float64
	}
	var overdueBuckets []OverdueSum
	a.db.Model(&Invoice{}).
		Select(`
			CASE
				WHEN days_outstanding BETWEEN 30 AND 60 THEN '30-60'
				WHEN days_outstanding BETWEEN 60 AND 90 THEN '60-90'
				WHEN days_outstanding > 90 THEN '90+'
			END as bucket,
			SUM(outstanding_bhd) as amount
		`).
		Where("status != ? AND days_outstanding >= 30", "Paid").
		Group("bucket").
		Having("bucket IS NOT NULL").
		Scan(&overdueBuckets)

	data.Overdue = make([]OverdueBucket, len(overdueBuckets))
	for i, b := range overdueBuckets {
		data.Overdue[i] = OverdueBucket{
			Days:   b.Bucket,
			Amount: b.Amount,
		}
	}

	return data, nil
}

// ExportReport exports report data to CSV format
// PDF/Excel will be added later using existing generator functions
func (a *App) ExportReport(reportType string, format string, dataJSON string) (string, error) {
	// RBAC: only users with access to the underlying data may export it.
	if err := a.requireReportAccess(reportType); err != nil {
		return "", err
	}
	// Whitelist known export report types (defence against injection/unknown types).
	if !isKnownExportReportType(reportType) {
		return "", newError("INVALID_REPORT_TYPE", "Unknown report type", reportType)
	}
	// Cap the incoming payload so a hostile/oversized dataJSON cannot exhaust memory.
	if len(dataJSON) > maxReportExportPayloadBytes {
		return "", newError("EXPORT_TOO_LARGE", "Report export payload is too large", "Reduce filters and try again")
	}

	// Ensure exports directory exists
	exportDir := a.getExportDir("report", "", "", time.Now().Year())

	// Generate filename — sanitize reportType so it can never inject path
	// separators or traversal sequences into the output filename.
	timestamp := time.Now().Format("2006-01-02_150405")
	safeReportType := sanitizeFileName(strings.ToLower(strings.TrimSpace(reportType)))
	if safeReportType == "" || safeReportType == "unnamed" {
		safeReportType = "report"
	}
	filename := fmt.Sprintf("%s_report_%s.%s", safeReportType, timestamp, format)
	exportPath := filepath.Join(exportDir, filename)

	switch format {
	case "csv":
		return a.exportReportToCSV(exportPath, reportType, dataJSON)
	case "pdf":
		return "", newError("NOT_IMPLEMENTED", "PDF export coming soon", "Use CSV for now")
	case "excel":
		return "", newError("NOT_IMPLEMENTED", "Excel export coming soon", "Use CSV for now")
	default:
		return "", newError("INVALID_FORMAT", "Unsupported export format", format)
	}
}

// maxReportExportPayloadBytes caps the size of a report export request payload
// so a hostile or malformed dataJSON cannot exhaust memory during export.
const maxReportExportPayloadBytes = 15 * 1024 * 1024

// requireReportAccess enforces RBAC on report export/generation, mapping each
// report type to the permission that gates access to its underlying data.
func (a *App) requireReportAccess(reportType string) error {
	switch strings.ToLower(strings.TrimSpace(reportType)) {
	case "financial":
		return a.requirePermission("finance:view")
	case "sales":
		return a.requirePermission("offers:view")
	case "operations":
		return a.requirePermission("orders:view")
	case "customers":
		return a.requirePermission("customers:view")
	case "inventory":
		return a.requirePermission("inventory:view")
	default:
		return a.requirePermission("reports:view")
	}
}

// isKnownExportReportType whitelists the report types the exporter can produce.
func isKnownExportReportType(reportType string) bool {
	switch strings.ToLower(strings.TrimSpace(reportType)) {
	case "sales", "customers", "operations", "inventory", "financial":
		return true
	default:
		return false
	}
}

