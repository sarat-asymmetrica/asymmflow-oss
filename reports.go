// ReportData encapsulates all report types (sales, customers, operations, etc.)
type ReportData struct {
	// Sales Report Data
	Pipeline       []PipelineStage `json:"pipeline,omitempty"`
	ConversionRate float64         `json:"conversion_rate,omitempty"`
	AvgDealSize    float64         `json:"avg_deal_size,omitempty"`
	WinRate        float64         `json:"win_rate,omitempty"`
	TopProducts    []TopProduct    `json:"top_products,omitempty"`

	// Customer Report Data
	GradeDistribution []GradeDistItem    `json:"grade_distribution,omitempty"`
	TypeDistribution  []CustomerTypeItem `json:"type_distribution,omitempty"`
	AvgPaymentDays    float64            `json:"avg_payment_days,omitempty"`
	CollectionEff     float64            `json:"collection_efficiency,omitempty"`

	// Operations Report Data
	OrdersByStage    []OrderStage `json:"orders_by_stage,omitempty"`
	AvgLeadTime      int          `json:"avg_lead_time,omitempty"`
	OnTimeDelivery   float64      `json:"on_time_delivery,omitempty"`
	PendingShipments int          `json:"pending_shipments,omitempty"`

	// Inventory Report Data
	TotalItems     int                    `json:"total_items,omitempty"`
	TotalValue     float64                `json:"total_value,omitempty"`
	LowStockAlerts int                    `json:"low_stock_alerts,omitempty"`
	Movements      []StockMovementSummary `json:"movements,omitempty"`

	// Financial Report Data — trader vocabulary, not SaaS metrics. No runway/
	// burn/MRR-target here: those were either hardcoded fiction (Runway) or
	// rough guesses (Cash Balance, Monthly Burn) dressed up as real numbers.
	// Receivables/Payables Outstanding and Avg Monthly Revenue below are all
	// real query results.
	ReceivablesOutstanding float64         `json:"receivables_outstanding,omitempty"`
	PayablesOutstanding    float64         `json:"payables_outstanding,omitempty"`
	AvgMonthlyRevenue      float64         `json:"avg_monthly_revenue,omitempty"`
	CollectionTarget       float64         `json:"collection_target,omitempty"`
	Collected              float64         `json:"collected,omitempty"`
	Overdue                []OverdueBucket `json:"overdue,omitempty"`
}

